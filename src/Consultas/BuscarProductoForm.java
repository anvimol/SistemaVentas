    private void buscarProducto() {
        int fila;
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        fila = tblBuscarProd.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Se debe seleccionar un registro");
        } else {
            defaultTableModel = (DefaultTableModel) tblBuscarProd.getModel();

            ComprasForm.txtCodProducto.setText((String) defaultTableModel.getValueAt(fila, 1));
            ComprasForm.txtNombreProducto.setText((String) defaultTableModel.getValueAt(fila, 2));
            ComprasForm.txtStock.setText((String) defaultTableModel.getValueAt(fila, 4));
            ComprasForm.txtPrecio.setText((String) defaultTableModel.getValueAt(fila, 6));

        }
    }

