    private void tblBuscarProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBuscarProdMouseClicked
        if (tblBuscarProd.getSelectedRows().length > 0) {
            buscarProducto();
        }
        if (evt.getClickCount() == 2) {
            buscarProducto();
            this.dispose();
        }
    }//GEN-LAST:event_tblBuscarProdMouseClicked

    private void tblBuscarProdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblBuscarProdKeyReleased
        if (tblBuscarProd.getSelectedRows().length > 0) {
            buscarProducto();
        }
    }//GEN-LAST:event_tblBuscarProdKeyReleased

